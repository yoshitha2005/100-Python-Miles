def capitalize_Words():
    word=input("Enter a Word:")
    captal_words=word.upper()
    print(f"Capitalized Word is :{captal_words}")
capitalize_Words()

def capitalize_Words1():
    text = input("Enter a text: ")
    title_case = text.title()
    print(f"Title Case Word is: {title_case}")

capitalize_Words1()

def capitalize_Words2():
    letter = input("Enter a letter: ")
    captalize_case = letter.capitalize()
    print(f"Title Case Word is: {captalize_case}")

capitalize_Words2()